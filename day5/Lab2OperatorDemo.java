package day5;

public class Lab2OperatorDemo {

	public static void main(String[] args) {

		int a = 100;

		a += 10;
		System.out.println("a += 10 => " + a);

		a -= 10;
		System.out.println("a -= 10 => " + a);

		a *= 10;
		System.out.println("a*=10 => " + a);

		a /= 10;
		System.out.println("a/=10 => " + a);
	}
}
